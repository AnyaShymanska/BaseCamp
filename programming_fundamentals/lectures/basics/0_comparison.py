x = 5
y = 8

print("Results for x={}, y={}".format(x,y))
print("x == y:", x == y)  # False
print("x != y:", x != y)  # True
print("x < y:", x < y)  # True
print("x > y:", x > y)  # False
print("x <= y:", x <= y)  # True
print("x >= y:", x >= y)  # False
